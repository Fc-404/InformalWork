        <h1>京剧的发展</h1>
        <h2 id="s1-1">孕育期</h2>
        <p>
          清朝初期,京城戏曲舞台上盛行的是昆曲与京腔。1790年，乾隆爷八十大寿，扬州著名戏曲表演剧团“三庆”班被征召入京贺寿演出，从此便立足京城。此后许多徽班相继来到京城，其中以三庆、四喜、春台、和春最负盛名，这就是后来著名的“四大徽班”。他们在表演艺术上广泛吸取其它各家剧种的长处，将秦腔和汉剧这两种地方剧种融合，徽、秦、汉的合流，为京剧的诞生奠定了基础。
        </p>
        <img src="../assets/img/a-1-1.jpg">
        <h2 id="s1-2">形成期</h2>
        <p>
          在徽、汉合流之后,京剧又借鉴吸收了昆曲、京腔的长处，使得京剧正式形成。晚清民间画师沈蓉圃绘制了一幅画，叫《同光名伶十三绝》，是京剧史上的一幅名伶彩色剧装写真画。画中挑选了徽班进京后享有盛名的十三位京剧演员。画中演员的扮相、面部表情、服装特点等等，生动地展现了早期每位京剧演员的人物性格特点。《同光名伶十三绝》这幅画的诞生，为研究京剧、昆曲早期的服饰、扮相和各行角色的艺术特征留下了非常珍贵的形象资料。
        </p>
        <img src="../assets/img/a-1-2.jpg">
        <p>
          图中人物自左至右分别为郝兰田(《行路》康氏)、张胜奎(《一捧雪》莫成)、梅巧玲(《雁门关》萧太后)、刘赶三(《探亲》乡下妈妈)、余紫云(《彩楼》王宝钏)、程长庚(《群英会》鲁肃)、徐小香(《群英会》周瑜)、时小福(《采桑》罗敷)、杨鸣玉(《思志诚》明天亮)、卢胜奎(《空城计》诸葛亮)、朱莲芬(《琴挑》陈妙常)、谭鑫培(《恶虎村》黄天霸)、杨月楼(《探母》杨延辉)。
        </p>
        <h2 id="s1-3">成熟期</h2>
        <p>慈禧老佛爷可是大清朝的“骨灰级”戏迷，在她执政期间，先后有150多位名家入宫演出教学。同时，位于北京大栅地区的戏园，日日有演出。京剧占领了从宫廷到地方的大小舞台，逐渐形成一统的局面。</p>
        <img src="../assets/img/a-1-3.jpg">
        <p>
          在这一时期，谭鑫培、汪桂芬、孙菊仙是这个时期的代表人物，时称“老生后三杰”。其中，谭鑫培创造出独具演唱艺术风格的“谭派”，后被冠名“伶界大王”。旦角、生角和武生也开始崛起。各行名家,在继承中有创新发展,演唱技艺日臻成熟,将京剧推向新的高度。
        </p>
        <img src="../assets/img/a-1-4.jpg">
        <h2 id="s1-4">鼎盛期</h2>
        <p>京剧人才辈出，优秀演员的大量涌现，标志着京剧进入到鼎盛期，一时期流派纷呈，人才济济。在这一时期，主要的代表人物有“四大名旦”“四大须生”以及“武生泰斗”等。</p>
        <p>1927年，北京《顺天吋报》评选京剧旦角名伶，梅兰芳、尚小云、程砚秋、荀慧生这四位被评选成为“四大名旦”，后被简称为“梅、尚、程、荀”。这四位创造出的女性人物各具特色，逐渐形成了自己所特有的艺术风格。</p>
        <img src="../assets/img/a-1-5.jpg">
        <p>梅兰芳（中后），程砚秋（中前），荀慧生（右），尚小云（左）20年代的时候最佳男主角被称为“前四大须生”，是由老生演员余叔岩、马连良、言菊朋、高庆奎组成，简称“余、马、言、高”。</p>
        <img src="../assets/img/a-1-6.jpg">
        <p>将武生表演艺术发展到新高度的杨小楼，成为京剧界的最佳武生演员，被誉为“武生泰斗”、“国剧宗师”。</p>
        <img src="../assets/img/a-1-7.jpg">
        <p>
          京剧念白:念白是指中国传统戏曲人物中内心独白或两者对话，就中国传统戏曲而言，是指人物内心独白或两者对话，使用明显节奏变化，并拖长字音的语调。此种介于口白与唱腔的表演方式，很难区分。一般来说，不具梆子、西皮、二黄等基础唱调，但是比口白夸张的拉长尾音说话方式，皆可称为念白。（念白部分视频主要以念白读诗为主）
        </p>
