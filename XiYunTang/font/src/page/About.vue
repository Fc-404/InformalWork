        <h1>京剧派别</h1>
        <h2 id="s3-1">京剧分哪几派?</h2>
        <p>
          谭派创始人——谭鑫培（1847——1917）。名金福，湖北江夏（今武昌）人。清“内廷供奉。”艺术上刻苦钻研、勇于创新，讲究音律，能吸收前辈艺人和其他戏曲、曲艺的表演方法、腔调，融会贯通，使唱、做、念、打有机结合，发展了京剧老生的表演艺术。善于根据剧情需要刻画人物，既注重京剧艺术程式，又富有生活气息。唱腔悠扬宛转，饶有韵味，有些则略带感伤，有“云遮月”之称。1900年前后成为京剧界主要代表人物，世称“谭派”。演出剧目《李陵碑》、巜秦琼卖马》、《空城计》、《定军山》、巜汾河湾》、巜桑园寄子》、《战大平》等最著名。陈彦衡曾记录谭的十一个剧目乐谱为巜说谭》、《戏选》、《探母回令》等书，后除《探母回令》外，其余合编为《谭鑫培唱腔集》。
        </p>
        <p>
          言派创始人——言菊朋（1890——1942）原名锡，北京人。蒙族。曾在清廷蒙藏院任职。因酷爱京剧，业余参加清音雅集、春阳友会等票房。演老生。早年经常观摩谭鑫培演出，并从陈彦衡学“谭派”戏，又向红豆館主、钱金福、王长林等请益，唱、做、唸、打均有基础。1923年，在梅兰芳、陈彦衡等鼓励下，正式参加戏班。以演《汾河湾》、《战太平》、《定军山》、《桑园寄子》、《法场換子》等著名。他文化素养较高，尤精通音律。（上世纪）二十年代未自己挑班后，又在“谭派”基础上，吸收其他行当和京韵大鼓的唱念方法，自创新腔，婉转跌宕、细腻精巧，世称“言派”。独擅剧目《卧龙吊孝》、巜让徐州》等均为“谭派”老生所不演，独辟蹊径，深为世人所推重。
        </p>
        <p>
          余派创始人——余叔岩（1890——1943）名第祺，湖北罗田人，生于北京。名演员余三胜之孙、余紫云之子。自幼倾慕谭鑫培，并从姚增禄学艺。初以“小小余三胜”艺名在天津演唱，倒嗓后休养，向前辈钱金福、王长林、陈彦衡等请教，拜谭鑫培为师。1915年重行登台，演出《打掍出箱》而名重一时。1916年谭去世后，他成为“谭派”主要传人。继承了唱做并重、文武兼长的艺术特点，并形成自己新的艺术风格，善于以唱腔表达人物思想感情，世称“余派”。以《问樵闹府》、《盗宗卷》、《战太平》、《定军山》、《桑园寄子》等剧最著名。
        </p>
        <h2 id="s3-2">梅派</h2>
        <img src="../assets/img/a-3-1.jpg">
        <p>梅兰芳祖籍泰州，1894年出生于北京的一个梨园世家。在展览的“群英谱京韵”“梅秀出世家”单元，沈蓉圃绘制的《同光十三绝》和《思志诚》等珍贵展品，既是对京剧历史的追溯，也是对梅兰芳家世介绍的铺垫。</p>
        <p>
          作为20世纪初新兴旦行流派先驱，梅兰芳在继承传统的同时，编演了多部时装新戏和古装新戏，对京剧表演、舞台、服饰、容妆、伴奏等方面进行了大胆的改革和创新，将京剧旦行表演艺术推向了崭新的高度。“梅占百花魁”单元中，梅兰芳改良虞姬古装衣、梅兰芳的上色照片《四郎探母》《一缕麻》剧照、虞姬双剑等展品，无一不彰显出梅兰芳对美的见解和对时代的引领。
        </p>
        <p>“梅先生将多个京剧行当集于一身，他的革新是在此基础上进行的。他在绘画、书法、诗词方面的涵养也都体现在了舞台艺术当中。”中国国家博物馆副研究馆员、策展人诸葛英良说。</p>
        <p>
          梅兰芳曾在《要善于辨别精、粗、美、恶》中写道：我个人的体会，不论演员或剧作者都要努力开展自己的眼界，除了多看多学多读，还可以在戏曲范围之外，去接触各种艺术品和大自然的美景，来多方面培养自己的艺术水平。展览对梅兰芳借物喻情，将生活中的发现和体悟融入艺术创作进行了展示。此外，观众还可以在“梅骨傲风雪”单元中看到梅兰芳的书画作品《春消息》《松柏有本性》《和平鸽》等，清丽脱俗、温润素雅。
        </p>
        <p>
          博大精深的中华文明是中华民族独特的精神标识，是当代中国文艺的根基，也是文艺创新的宝藏。此次展览中可以看到，从五四运动后的《一缕麻》《宦海潮》等打破封建迷信、封建礼教的时装戏，到抗战初期的《生死恨》《抗金兵》等鼓舞全民族斗志的历史剧，梅兰芳剧团的排演剧目始终与时代变革相关、以家国兴亡为系。
        </p>
        <p>在中国国家博物馆党委书记、副馆长单威看来，梅兰芳在忠实继承了传统文化基础上推陈出新，适应时代变化和广大人民群众的艺术期待，对当下在文化建设当中坚持守正创新，具有重大启示和借鉴意义。</p>
        <p>
          梅兰芳创立的“梅派”艺术，是京剧旦行中首先形成的，影响极其深远的京剧流派。“梅派”主要是综合了青衣、花旦和刀马旦的表演方式，在唱、念、做、舞、音乐、服装、扮相等各个方面，进行不断的创新和发展，将京剧旦行的唱腔、表演艺术提高到了一个全新的水平，达到了完美的境界。
        </p>
        <p>代表剧目有《宇宙锋》《霸王别姬》《贵妃醉酒》《女起解》《水斗断桥》《奇双会》《廉锦枫》《天女散花》《木兰从军》《抗金兵》《生死恨》《西施》《洛神》《穆桂英挂帅》等。昆曲有《思凡》《乔醋》《闹学》《游园惊梦》等。</p>
        <h2 id="s3-3">程派</h2>
        <p>
          程派程砚秋创立，旦角，声情美永的艺术特色。程派的唱腔别具一格。他严守音韵规律，随着戏剧情节和人物情绪的发展变化，唱腔起伏跌宕，节奏多变，要求达到“声、情、美、永”的高度结合。他的表演非常细致深刻，讲究舞台表现形式的完整与美感，同时注重贴近生活的真实。编创《文姬归汉》《荒山泪》《春闺梦》《亡蜀鉴》等剧目。
        </p>
        <p>
          艺术特色：程砚秋在艺术创作上，勇于革新创造，舞台表演唱腔讲究音韵，注重四声，并根据自己独有的嗓音特点，创造出了一种幽雅婉转、若断若续的唱腔风格，形成独有的特点。他创作的角色，典雅娴静，恰如霜天白菊，有一种清峻之美，后成为“四大名旦”之一。程砚秋在表演上无论眼神、身段、步法、指法、水袖、剑术等方面也都有一系列的创造和与众不同的特点，作为一个完整的艺术流派，全面展现在京剧艺术舞台上。
          程砚秋注重借鉴兄弟姊妹艺术，融合于自己的艺术创作之中，是众多艺术大师中较为突出的一位。</p>
        <p>代表剧目：《彩楼配》《二进宫》《祭塔》《女起解》《玉堂春》《战蒲关》《四郎探母》《桑园会》《御碑亭》《宝莲灯》《孔雀东南飞》《秋江》《绝路问苍天》《江姐》《白蛇传》《梁祝》</p>
        <h2 id="s3-4">荀派</h2>
        <p>荀慧生（1900年1月5日一1968年12月26日），出生于河北省阜城县，中国京剧演员，工旦角、四大名旦之一、荀派创始人。</p>
        <p>他大胆破除传统局限，发挥个人嗓音特长，吸取昆、梆、汉、川等曲调旋律，大胆创新，塑造的许多少女、少妇的艺术形象，娇雅妩媚、清秀俊美、风格各异。</p>
        <p>
          艺术特点：他对唱和念的要求非常严格，认为唱要“以有限的腔调曲表达繁复错综的感情”，而念白在与观众交流时“比歌唱更直接、更有力一些”。为此，他主张唱和念时生活感和艺术性两者结合，协调融洽，并与表情、身段的真实自然相一致，互为表里。这正是荀派表演的特殊风格。他在继承传统的基础上，在剧本、唱腔、表演、念白、到服饰等方面都进行了创新的实践与探索。在唱腔艺术方面荀慧生大胆破除传统局限，发挥个人嗓音特长，吸取昆、梆、汉、川等曲调旋律，大胆创新。这不是简单的一曲多用，而是从生活出发，从人物感情与心境出发，字正腔圆，腔随情出，令人着迷。他善于使用上滑下滑的装饰音，听来俏丽、轻盈、谐趣具有特殊的韵味。他还十分注重道白艺术，吐字清晰，声情并貌，他创造出融韵白、京白为一体的念白，韵调别致，具有特殊的表现力。
        </p>
        <p>一生演出了三百多出戏，其中代表作为《元宵谜》《玉堂春》《棋盘山》等，与杨小楼、尚小云、谭小培一起称为“三小一白”。</p>
        <p>六大喜剧、《元宵谜》《丹青引》《绣襦记》《勘玉钏》《红娘》《卓文君》；六大悲剧
          《钗头凤》《杜十娘》《鱼藻宫》《红楼二尤》《霍小玉》《晴雯》；六大武剧：《陶三春》《大英杰烈》《荀灌娘》《美人一丈青》《婚姻魔障》《盘丝洞》；六大传统剧：《贩马记》《玉堂春》《十三妹》《棋盘山》《得意缘》《金玉奴》；
          六大移植剧：《花田错》、《赵五娘》、《辛安驿》《元宵谜》《香罗带》《庚娘》；六大跌扑剧：《蝴蝶梦》《东方夫人》《九曲桥》《东吴女丈夫》《战宛城》《翠屏山》； 可见其能戏之多。</p>
        <h2 id="s3-5">尚派</h2>
        <p>
          尚派艺术的创始人尚小云，尚派行腔吐字清楚，尚派以嗓音清亮激越、旋律跌宕缭绕的传统，以板头的变化运用，打破唱腔的固定节奏，展示唱腔的丰富内涵；又以斩钉截铁的断和错综有力的顿挫，使唱腔错落有致，往往在平易简约、坚实整齐中呈现峭险之处，显得力透纸背。
        </p>
        <p>
          艺术特点：尚派打破了以往京剧旦行（青衣）专门讲究“贞女节烈”的道德评判标准，从“烈”之一端引发了“侠”、“义”、“刚”、“健”等内涵，从更广阔的层面关注妇女的生存和生活价值，实际上已隐含了对传统妇女观念的批判，颇具时代意义。大大增强了京剧旦角（青衣）一行的表现力，拓展了京剧旦行的表现空间。这是尚小云能够步入四大名旦的主要原因，也是他作为现代京剧艺术大师的精神基础。
        </p>
        <p>此外，在剧目内容上大胆取少数民族及域外生活为背景，这也是京剧史上的首创，对拓展京剧表现力作了可贵的探索。在新剧的排演中，大规模使用布景，并参照历史、民族服饰的实物，改革舞台服装及化妆，这也是对舞台的整体形象改革的尝试。
          尚派 京剧遵循艺术规律，同时开展了对传统剧目的加工与新编剧目的创作。对于传统戏，尚派 京剧或增头加尾，或连缀折子戏，或删繁剔冗，以使主题突出，情节完整，人物鲜明。</p>
        <p>经典剧目：尚派 京剧曾改编演出了《玉堂春》《十三妹》《梅玉配》《詹淑娟》《天河配》《雷峰塔》《春秋配》《乾坤福寿镜》等数十个传统剧目。而对于新编剧目的排演，尚派
          京剧根据自己文武并俪、各行皆工的特长精心创造，在唱腔、扮相、表演、布景等各方面都进行了一系列的探索和创造，极大丰富了京剧舞台。《梁红玉》是尚派名剧。30年代末尚小云根据传统折子戏《战金山》等整理改编，从《玉玲珑》起，到《战金山》结束，名《梁夫人》。
        </p>
        <p>1950年前后，又予以加工，易名《梁红玉》。其中《许婚从军》的唱段和《擂鼓助阵》的擂鼓表演尤为精妙。后来，尚派 根据意大利籍清宫画家郎世宁所绘的香妃半身戎装像改进了传统的女靠，使梁红玉的形象更加英俊挺拔。
          为了强化我国民族戏曲载歌载舞的特点，在尚派
          京剧的保留剧目中，常常采用昆剧的演法，有些场次整个按照昆剧折子戏来演。一些昆剧折子戏经他大幅度加工，成了京剧的常演剧目，而且都已成为尚派名剧，如《梁红玉》的后半出《战金山》，《汉明妃》的后半出《昭君出塞》，就是享誉最盛的艺术精品。
        </p>
