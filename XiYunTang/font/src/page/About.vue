        <h1>京剧行当</h1>
        <p>一部电影中，通常会有演员扮演不同的角色。和电影相似，京剧中也有许多不同的角色。这些角色在京剧中被称为行当。今天我们就来学习一下京剧的行当吧！</p>
        <p>不同行当化妆、服装、演唱和表演方面各具特色，分别为生、旦、净、丑。</p>
        <img src="../assets/img/a-2-1.jpg">
        <h2 id="s2-1">生行</h2>
        <img src="../assets/img/a-2-2.jpg">
        <p>“生行”按照年龄、身份的不同分为老生、小生、武生、红生、娃娃生等几类。</p>
        <img src="../assets/img/a-2-3.jpg">
        <p>老生是中年或年长的男性人物，比如诸葛亮、秦琼。他们用真声演唱，大多带着一个胡须，被称为“髯口”。</p>
        <img src="../assets/img/a-2-4.jpg">
        <p>小生是指那些饱读诗书、风流倜傥、眉目清秀、英俊潇洒的青少年男子，比如《西厢记》中的张生、《群英会》中的周瑜。他们不带胡须，在演唱和念白时使用假声和真声，声音高挑，动作更为灵活。</p>
        <img src="../assets/img/a-2-5.jpg">
        <p>武生是京剧中的男性“功夫”演员，按照扮相的不同，又分为长靠武生和短打武生。长靠武生一般身穿盔甲，脚蹬厚底靴，背叉靠旗，盔上叉翎子，一般为大将高官。短打武生则身穿紧身衣裤，动作轻快敏捷，武功矫健，江湖豪杰、侠义英雄。
        </p>
        <p>生行中还有几类比较特殊的角色，比如红生和娃娃生，前者指专门用红色勾画脸谱的角色，大多为关公戏，后者为小演员或个子较小女演员扮演的天真烂漫的少年儿童，如《小放牛》中的牧童，《三娘教子》中的倚哥等。</p>
        <img src="../assets/img/a-2-6.jpg">
        <h2 id="s2-2">旦行</h2>
        <p>“旦行”一般表示女性角色，分为青衣、花旦、武旦、老旦、刀马旦、花衫等。</p>
        <img src="../assets/img/a-2-7.jpg">
        <p>“青衣”多表现为内敛婉约的中、青年妇女，她们动作端庄，动作幅度有限，大多只可把手指的指间露出水袖，不能任意伸展和有幅度大的动作。青衣以唱为主，比如《武家坡》中的王宝钏、《汾河湾》中的柳迎春等。</p>
        <img src="../assets/img/a-2-8.jpg">
        <p>京剧中将未婚、年轻、天真的女孩称为“花旦”她们在京剧表演中多念白，表演时眼神犀利，腰肢、脚下灵巧，演唱声色清脆、透亮。比如《拾玉镯》中的孙玉姣等。</p>
        <img src="../assets/img/a-2-9.jpg">
        <p>“武旦”是指那些会武艺的女性，既有女性之美，又有武将之威武，她们善于翻扑跌打，经常需要踩着跷“打出手”。如《虹桥赠珠》里的水母、《武松打店》里的孙二娘等</p>
        <img src="../assets/img/a-2-10.jpg">
        <p>沉稳年长的妇女在京剧中被称为“老旦”。这一行当的特点延续了演唱苍劲、硬朗、洪亮，动作沉稳的表演特点。比如《杨门女将》中的佘太君。</p>
        <img src="../assets/img/a-2-11.jpg">
        <p>随着京剧的发展，角色日渐丰富，出现了涵盖两种或以上行当的角色。比如刀马旦。刀马旦多穿着靠，顶盔贯甲。这个角色，一般都骑马，拿着一把尺寸比较小的刀，所以被称为“刀马旦”，通常表现为一些雄姿飒爽的女将。比如“穆桂英”等。
        </p>
        <p>京剧表演艺术家王瑶卿为了丰富旦角的表演艺术，充实艺术表现能力，融和了青衣、花旦、刀马旦，创作出一种多种行当并重的旦角行当，人们把它命名为花衫。</p>
        <p>总的来说，京剧的行当分为生、旦、净、丑。其中生行为男性演员，又按照年龄和角色定位的不同分为老生、小生、武生、红生、娃娃生等。旦角为女性演员，划分为青衣、花旦、武旦、老旦、刀马旦、花衫等。</p>
        <h2 id="s2-3">净行</h2>
        <p>除去生行和旦行外，京剧中还有两个行当不得不提，那便是净行和丑行。</p>
        <p>我国著名作家冯骥才讲述了《花脸》的故事，年少新年时买了一张关羽的“花脸”和一把“青龙偃月刀”,从而成了过年的主角。
          故事中的关羽在京剧中一般由两个特殊的行当扮演,一个叫红生,属于前面提到过的生行，另一个叫红净，也就是京剧里的“净行”，属于花脸。</p>
        <img src="../assets/img/a-2-12.jpg">
        <p>“花脸”是由唐代乐舞“代面”所戴的面具演变而来。京剧演员用各种色彩在面部勾画成一定的图案，借以显现性格、品质或相貌有突出特征的男性人物。</p>
        <img src="../assets/img/a-2-13.jpg">
        <p>花脸主要分为铜锤花脸、架子花脸和武花脸。</p>
        <p>
          铜锤花脸的称呼来自京剧人物徐延昭。他袭爵定国公，世代忠良，他家有一个皇帝亲赐的大铜锤，“上打昏君、下打奸臣”，徐彦昭拿着它高唱“铜锤打你个碎纷纷”。这样以唱工为主的花脸角色，称为“铜锤花脸”，扮演的多是端庄稳重，刚直不阿的朝中重臣，如包拯、姚期等。
        </p>
        <img src="../assets/img/a-2-14.jpg">
        <p>架子花脸又称“副净”，以念白和做工见长，多扮演勇猛豪爽、刚烈彪悍、纯真诙谐的人物，时常大叫 “哇哈哈”。比如《芦花荡》中的张飞、《李逵探母》中的李逵等。</p>
        <img src="../assets/img/a-2-15.jpg">
        <p>架子花脸中还有一类角色称为勾水白脸，俗称“白净”，多为阴险狡诈、笑里藏刀之人，最典型的人物莫过于白脸的曹操了。</p>
        <img src="../assets/img/a-2-16.jpg">
        <p>武花脸一般为莽汉，能摔能打，能翻能跳，常扮演鲁莽或凶狠的角色，如《挑滑车》里的黑风利，《火烧余洪》里的余洪等。</p>
        <img src="../assets/img/a-2-17.jpg">
        <h2 id="s2-4">丑行</h2>
        <p>生活中见过的小丑，他们脸上也有颜料，十分滑稽和古怪。在京剧的舞台上也有这样一种角色，他被称为丑行。</p>
        <p>京剧中的“丑行”被称为"小花脸"。"丑行”的化妆虽与大花脸有点相象，可是他的表演风格却完全不同，有点像夸张的漫画。</p>
        <img src="../assets/img/a-2-18.jpg">
        <p>丑行大多在鼻梁上抹一块白粉，十分滑稽风趣。传说当年唐玄宗好演戏，扮丑角，为不损国威，鼻前挂白玉，这就是丑行所画脸谱中的白色豆腐块。</p>
        <p>丑行一般分为文丑和武丑。</p>
        <p>文丑以念白为主，经常扮演具有狡猾奸诈特性的人物，比如《群英会》中的蒋干、《审头刺汤》里的汤勤。</p>
        <p>武丑主要扮演武功精湛，性格诙谐、机智的绿（lu）林好汉、侠盗小偷等等。武丑不仅要有清脆的念白，还要有蹿、蹦、跳、跃的武功，身怀绝技。比如《时迁偷鸡》中的时迁、《三岔口》中的刘利华等。</p>
        <img src="../assets/img/a-2-19.jpg">
        <p>除此之外，一些滑稽逗笑的妇女也由丑行扮演，被称为“彩旦“，比如《拾玉镯》中的刘妈妈、《凤还巢》里的大小姐程雪雁等。</p>
        <img src="../assets/img/a-2-20.jpg">
        <p>京剧中勾画脸谱的绝大多数属于净角和丑角。净角以面部化妆运用图案化的脸谱为标志，扮演男性人物，包括铜锤花脸、架子花脸、武花脸。</p>
        <p>京剧中的"丑"行演员又称为"小花脸"，包括文丑和武丑，主要以夸张的表演和滑稽的动作引人入胜。</p>
